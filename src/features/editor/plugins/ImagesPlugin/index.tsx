/**
 * Images Plugin Architecture
 *
 * This plugin is split into multiple files for clarity:
 *
 * 1. index.tsx (this file): Core image functionality
 *    - Defines INSERT_IMAGE_COMMAND (inserts image into editor)
 *    - Defines SHOW_INSERT_IMAGE_DIALOG_COMMAND (shows the popover)
 *    - Handles drag & drop for images
 *    - Registers image node
 *
 * 2. insert-image-listener.tsx: Command listener
 *    - Listens for SHOW_INSERT_IMAGE_DIALOG_COMMAND
 *    - Shows popover above cursor position
 *    - Manages popover state
 *
 * 3. insert-image-popover.tsx: UI Component
 *    - Renders the popover UI (URL/File upload options)
 *    - Positioned above the current line
 *    - Calls onInsert when user submits
 *
 */

import type { JSX } from "react";

import {
  $isAutoLinkNode,
  $isLinkNode,
  LinkNode,
  TOGGLE_LINK_COMMAND,
} from "@lexical/link";
import { useLexicalComposerContext } from "@lexical/react/LexicalComposerContext";
import {
  $findMatchingParent,
  $wrapNodeInElement,
  mergeRegister,
} from "@lexical/utils";
import {
  $createParagraphNode,
  $createRangeSelection,
  $getSelection,
  $insertNodes,
  $isNodeSelection,
  $isRootOrShadowRoot,
  $setSelection,
  COMMAND_PRIORITY_EDITOR,
  COMMAND_PRIORITY_HIGH,
  COMMAND_PRIORITY_LOW,
  createCommand,
  DRAGOVER_COMMAND,
  DRAGSTART_COMMAND,
  DROP_COMMAND,
  getDOMSelectionFromTarget,
  isHTMLElement,
  LexicalCommand,
  LexicalEditor,
} from "lexical";
import { useEffect } from "react";

// import landscapeImage from '../../images/landscape.jpg';
